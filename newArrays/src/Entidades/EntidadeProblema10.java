package Entidades;

public class EntidadeProblema10 {

	private String name;
	private double pNota;
	private double sNota;
	
	public EntidadeProblema10(String name, double pNota, double sNota) {
		this.name = name;
		this.pNota = pNota;
		this.sNota = sNota;
	}
	
	public String getName() {
		return name;
	}
	public double getpNota() {
		return pNota;
	}
	public double getsNota() {
		return sNota;
	}
	
	
}
