package Entidades;

public class EntidadeDesafio1 {

	private String name;
	private String email;
	private int quarto;
	
	
	public EntidadeDesafio1(String name, String email, int quarto) {
		this.name = name;
		this.email = email;
		this.quarto = quarto;
	}

	public String getName() {
		return name;
	}

	public String getEmail() {
		return email;
	}

	public int getQuarto() {
		return quarto;
	}
	
	
}
